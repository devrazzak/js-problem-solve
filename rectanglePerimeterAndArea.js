// Calculate a Area of Rectangle
function RectangleArea(lenght, width) {
    const area = lenght * width;
    return area;
}

console.log(RectangleArea(5, 10));

// Calculate Perimeter of Rectangle
function RectanglePerimeter(lenght, width) {
    const perimeter = 2 * (lenght + width);
    return perimeter;
}

console.log(RectanglePerimeter(5, 20));
